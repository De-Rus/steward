export const DICTS: Record<string, Dict> = { es, en }
const DEFAULT_LOCALE = 'es'

export type TFn = (key: string, vars?: Record<string, unknown>) => string

function interpolate(template: string, vars?: Record<string, unknown>): string {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (m, k: string) => (k in vars ? String(vars[k]) : m))
}

export function makeT(locale?: string | null, overrides?: Record<string, string> | null): TFn {
  const loc = locale && DICTS[locale] ? locale : DEFAULT_LOCALE
  return (key, vars) => {
    const template =
      overrides?.[key] ?? DICTS[loc]?.[key] ?? DICTS[DEFAULT_LOCALE][key] ?? key
    return interpolate(template, vars)
  }
}

const I18nContext = createContext<TFn>(makeT())

export function I18nProvider({
  locale,
  strings,
  children,
}: {
  locale?: string | null
  strings?: Record<string, string> | null
  children: React.ReactNode
}) {
  const t = useMemo(() => makeT(locale, strings), [locale, strings])
  return <I18nContext.Provider value={t}>{children}</I18nContext.Provider>
}

export function useT(): TFn {
  return useContext(I18nContext)
}
